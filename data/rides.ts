export function getRidesByPark(parkId: ParkId): Ride[] {
  return RIDES.filter(r => r.parkId === parkId);
}

export function getRide(id: string): Ride | undefined {
  return RIDES.find(r => r.id === id);
}

export function getLLSPRides(parkId: ParkId): Ride[] {
  return RIDES.filter(r => r.parkId === parkId && r.llType === 'LLSP');
}

export function getRopeDropRides(parkId: ParkId): Ride[] {
  return RIDES.filter(r => r.parkId === parkId && r.ropeDropPriority !== null)
    .sort((a, b) => (a.ropeDropPriority ?? 99) - (b.ropeDropPriority ?? 99));
}
